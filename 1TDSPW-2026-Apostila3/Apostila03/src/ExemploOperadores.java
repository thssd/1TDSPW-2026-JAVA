public class ExemploOperadores {

    public static void main(String[] args) {

        int n = 100;

        n++; //Incrementa a variavel em 1 unidade
        System.out.println(n);
        n--; //Decrementa a variavel em 1 unidade
        System.out.println(n);

        //Somar 10 no n
        n += 10; //n = n + 10;
        System.out.println(n); // ?
        n -= 50;
        System.out.println(n); // ?
        n *=3;
        System.out.println(n); // ?
        n /= 2;
        System.out.println(n); // ?
        n %= 7;
        System.out.println(n); // ?

    }
}
